    private void napuniKomboVrsta() {
        cbVrsta.removeAllItems();
        try {
            for (Vrsta v : Vrsta.values()) {
                cbVrsta.addItem(v.toString());
            }
        } catch (Exception ex) {
            Logger.getLogger(RezultatForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
