    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {
            if(txtNaziv.getText().isEmpty() || txtOpis.getText().isEmpty() || txtUputstvoZaPrimenu.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Morate popuniti sva polja oznacena sa *");
                return;
            }
            String naziv = txtNaziv.getText().trim();
            String opis = txtOpis.getText().trim();
            String uputstvoZaPrimenu = txtUputstvoZaPrimenu.getText().trim();
            String vrstaString = (String) cbVrsta.getSelectedItem();
            Vrsta vrsta = Vrsta.valueOf(vrstaString);
            String oznaka = txtOznaka.getText().trim();
            
            Test test = new Test(Long.MIN_VALUE, naziv, opis, uputstvoZaPrimenu, vrsta, oznaka);
            Kontroler.getInstance().sacuvajTest(test);
            int option = JOptionPane.showConfirmDialog(this, "Test je uspesno sacuvan. Da li zelite da unesete jos neki test?","",JOptionPane.YES_NO_OPTION);
            if(option == JOptionPane.YES_OPTION){
                txtNaziv.setText("");
                txtOpis.setText("");
                txtUputstvoZaPrimenu.setText("");
                txtOznaka.setText("");
            }else{
                this.dispose();
            }
        } catch (Exception ex) {
            Logger.getLogger(TestForma.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed


