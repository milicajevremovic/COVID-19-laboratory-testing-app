    private void napuniKomboLaboranti() {
        cbLaboranti.removeAllItems();
        try {
            List<Laborant> list = Kontroler.getInstance().dajSveLaborante();
            for (Laborant l : list) {
                cbLaboranti.addItem(l);
            }
        } catch (Exception ex) {
            Logger.getLogger(RezultatForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void srediFormu(ModForme mod) {

        switch (mod) {
            case UNOS:
                txtIme.setEnabled(true);
                txtPrezime.setEnabled(true);
                txtDatum.setEnabled(true);
                txtEmail.setEnabled(true);
                txtTelefon.setEnabled(true);
                cbLaboranti.setEnabled(true);
                btnSacuvajPacijenta.setVisible(true);
                setTitle("Dodavanje novog pacijenta");
                txtDatum.setForeground(Color.GRAY);
                txtDatum.setText("Na primer 28.10.1996");
                txtDatum.addFocusListener(new FocusListener() {
                    @Override
                    public void focusGained(FocusEvent e) {
                        if (txtDatum.getText().equals("Na primer 28.10.1996")) {
                            txtDatum.setText("");
                            txtDatum.setForeground(Color.BLACK);
                        }
                    }

                    @Override
                    public void focusLost(FocusEvent e) {
                        if (txtDatum.getText().isEmpty()) {
                            txtDatum.setForeground(Color.GRAY);
                            txtDatum.setText("Na primer 28.10.1996");
                        }
                    }
                });

                break;
            case IZMENA:
                txtIme.setEnabled(true);
                txtPrezime.setEnabled(true);
                txtDatum.setEnabled(true);
                txtEmail.setEnabled(true);
                txtTelefon.setEnabled(true);
                cbLaboranti.setEnabled(true);
                btnSacuvajPacijenta.setVisible(true);
                setTitle("Izmena pacijenta");
                for (Object k : session.Session.getInstance().getUseCaseParams().keySet()) {
                    System.out.println(k.toString());
                }
                
                Pacijent p = (Pacijent) session.Session.getInstance().getUseCaseParams().get("izabranipacijent");
                txtIme.setText(p.getIme());
                txtPrezime.setText(p.getPrezime());
                txtDatum.setText(Kontroler.getInstance().getSdf().format(p.getDatumRodjenja()));
                txtTelefon.setText(p.getTelefon());
                txtEmail.setText(p.getEmail());
                cbLaboranti.setSelectedItem(p.getLaborant());
                break;
            case PRIKAZ:
                Pacijent p1 = (Pacijent) session.Session.getInstance().getUseCaseParams().get("izabranipacijent");
                txtIme.setText(p1.getIme());
                txtPrezime.setText(p1.getPrezime());
                txtDatum.setText(Kontroler.getInstance().getSdf().format(p1.getDatumRodjenja()));
                txtTelefon.setText(p1.getTelefon());
                txtEmail.setText(p1.getEmail());
                cbLaboranti.setSelectedItem(p1.getLaborant());
                txtIme.setEnabled(false);
                txtPrezime.setEnabled(false);
                txtDatum.setEnabled(false);
                txtEmail.setEnabled(false);
                txtTelefon.setEnabled(false);
                cbLaboranti.setEnabled(false);
                btnSacuvajPacijenta.setVisible(false);
                setTitle("Prikaz pacijenta");

                break;
        }
    }
