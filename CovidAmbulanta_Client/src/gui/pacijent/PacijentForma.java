    private void btnSacuvajpacijentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajpacijentaActionPerformed
        try {
            if (txtIme.getText().isEmpty() || txtPrezime.getText().isEmpty() || txtDatum.getText().isEmpty() || txtTelefon.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Morate popuniti sva polja oznacena sa *.");
                return;
            }
            String ime = txtIme.getText().trim();
            String prezime = txtPrezime.getText().trim();
            Date datumR = null;
            try {
                datumR = Kontroler.getInstance().getSdf().parse(txtDatum.getText().trim());
                lblError.setText("");
            } catch (ParseException ex) {
                Logger.getLogger(PacijentForma.class.getName()).log(Level.SEVERE, null, ex);
                lblError.setForeground(Color.red);
                lblError.setText("Unesite datum u formatu dd.mm.gggg.");
                return;
            }
            String telefon = txtTelefon.getText().trim();
            String email = txtEmail.getText().trim();
            Laborant l = (Laborant) cbLaboranti.getSelectedItem();
            
            if (mod == ModForme.UNOS) {
                System.out.println("pre");
                Pacijent pacijent = new Pacijent(Long.MIN_VALUE, ime, prezime, datumR, telefon, email);
                
                pacijent.setLaborant(l);
                
                Kontroler.getInstance().sacuvajPacijenta(pacijent);
                System.out.println("posle");
                int option = JOptionPane.showConfirmDialog(this, "Pacijent je uspesno sačuvan. Da li zelite da unesete još nekog pacijenta?", "", JOptionPane.YES_NO_OPTION);
                if (option == JOptionPane.YES_OPTION) {
                    txtIme.setText("");
                    txtPrezime.setText("");
                    txtDatum.setText("");
                    txtTelefon.setText("");
                    txtEmail.setText("");
                } else {
                    this.dispose();
                }
                return;
            }
            if (mod == ModForme.IZMENA) {
                Pacijent pac = (Pacijent) session.Session.getInstance().getUseCaseParams().get("izabranipacijent");

                Pacijent p1 = new Pacijent(pac.getPacijentID(), ime, prezime, datumR, telefon, email);
                p1.setLaborant(l);

                Kontroler.getInstance().azurirajPacijenta(p1);
                JOptionPane.showMessageDialog(this, "Pacijent je ušpesno izmenjen.");
                PretragaForma parent = (PretragaForma) getParent();
                parent.osveziTabelu();
            }

        } catch (Exception ex) {
            Logger.getLogger(PacijentForma.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnSacuvajpacijentaActionPerformed

