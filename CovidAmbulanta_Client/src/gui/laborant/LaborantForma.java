    private void napuniKomboTestova() {
        cbTest.removeAllItems();
        try {
            List<Test> list = Kontroler.getInstance().dajSveTestove();
            for (Test test : list) {
                cbTest.addItem(test);
            }
        } catch (Exception ex) {
            Logger.getLogger(RezultatForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void srediFormu(ModForme mod) {
        switch (mod) {
            case UNOS:
                txtIme.setEnabled(true);
                txtPrezime.setEnabled(true);
                txtBrojDosijea.setEnabled(true);
                txtBrojOrdinacije.setEnabled(true);
                cbTest.setEnabled(true);
                btnSacuvajLaboranta.setVisible(true);
                setTitle("Dodavanje novog laboranta");
                break;
            case IZMENA:
                for (Object k : session.Session.getInstance().getUseCaseParams().keySet()) {
                    System.out.println(k);
                }
                Laborant l = (Laborant) session.Session.getInstance().getUseCaseParams().get("izabraniLaborant");
                txtIme.setEnabled(true);
                txtPrezime.setEnabled(true);
                txtBrojDosijea.setEnabled(true);
                txtBrojOrdinacije.setEnabled(true);
                cbTest.setEnabled(true);
                btnSacuvajLaboranta.setVisible(true);
                setTitle("Izmena laboranta");
                txtIme.setText(l.getIme());
                txtPrezime.setText(l.getPrezime());
                txtBrojDosijea.setText(l.getBrojDosijea());
                txtBrojOrdinacije.setText(l.getBrojOrdinacije() + "");
                cbTest.setEditable(true);
                cbTest.setSelectedItem(l.getTest());
                cbTest.setEditable(false);
                break;
            case PRIKAZ:
                txtIme.setEnabled(false);
                txtPrezime.setEnabled(false);
                txtBrojDosijea.setEnabled(false);
                txtBrojOrdinacije.setEnabled(false);
                cbTest.setEnabled(false);
                btnSacuvajLaboranta.setVisible(false);
                setTitle("Prikaz laboranta");

                break;
        }
    }
