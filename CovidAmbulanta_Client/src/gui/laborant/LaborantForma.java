    private void btnSacuvajLaborantaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajLaborantaActionPerformed
        try {
            if (txtPrezime.getText().isEmpty() || txtBrojDosijea.getText().isEmpty() || txtBrojOrdinacije.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Morate popuniti sva polja označena sa *.");
                return;
            }
            String ime = txtIme.getText().trim();
            String prezime = txtPrezime.getText().trim();
            String dosije = txtBrojDosijea.getText().trim();
            double ordinacija = Double.parseDouble(txtBrojOrdinacije.getText().trim());
            if(ordinacija < 0){
                lblError.setForeground(Color.red);
                lblError.setText("Broj ordinacije mora biti veci od 0.");
                return;
            }
            Test test = (Test) cbTest.getSelectedItem();
            if (mod == ModForme.UNOS) {
                Laborant la = new Laborant(1l, ime, prezime, dosije, ordinacija, test);
                Kontroler.getInstance().sacuvajLaboranta(la);
                lblError.setText("");
                int option = JOptionPane.showConfirmDialog(this, "Laborant je uspesno sacuvan! Da li želite da nastavite unos?", "", JOptionPane.YES_NO_OPTION);
                if(option == JOptionPane.YES_OPTION){
                    txtIme.setText("");
                    txtPrezime.setText("");
                    txtBrojDosijea.setText("");
                    txtBrojOrdinacije.setText("");
                }else{
                    this.dispose();
                }
                return;
            }
            if (mod == ModForme.IZMENA) {
                Laborant pre = (Laborant) session.Session.getInstance().getUseCaseParams().get("izabraniLaborant");

                Laborant la = new Laborant(pre.getLaborantID(), ime, prezime, dosije, ordinacija, test);
                Kontroler.getInstance().azurirajLaboranta(la);
                lblError.setText("");
                JOptionPane.showMessageDialog(this, "Podaci o zadatom laborantu su izmenjeni.");
                PretragaForma parent = (PretragaForma) getParent();
                parent.osveziTabelu();
            }

        } catch (Exception ex) {
            Logger.getLogger(LaborantForma.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnSacuvajLaborantaActionPerformed


